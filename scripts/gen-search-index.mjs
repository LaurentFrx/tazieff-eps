// This script generates the search index used by the client-side search modal.
// Called from prebuild/predev hooks in package.json.

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import matter from "gray-matter";

const root = process.cwd();
const outputDir = join(root, "src", "lib", "search");
const outputPath = join(outputDir, "search-index.ts");

/** @returns {Array<{slug: string, title: string, type: string, href: string, searchText: string}>} */
function collectExercices() {
  const dir = join(root, "content", "exercices");
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith(".fr.mdx"))
    .map((f) => {
      const raw = readFileSync(join(dir, f), "utf8");
      const { data } = matter(raw);
      const slug = data.slug || f.replace(".fr.mdx", "");
      const title = data.title || slug;
      const tags = Array.isArray(data.tags) ? data.tags.join(" ") : "";
      const muscles = Array.isArray(data.muscles) ? data.muscles.join(" ") : "";
      return {
        slug,
        title,
        type: "exercice",
        href: `/exercices/${slug}`,
        searchText: `${title} ${tags} ${muscles}`,
      };
    });
}

function collectMethodes() {
  const dir = join(root, "content", "methodes");
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith(".fr.mdx"))
    .map((f) => {
      const raw = readFileSync(join(dir, f), "utf8");
      const { data } = matter(raw);
      const slug = data.slug || f.replace(".fr.mdx", "");
      const titre = data.titre || slug;
      const description = data.description || "";
      const mots_cles = Array.isArray(data.mots_cles) ? data.mots_cles.join(" ") : "";
      const categorie = data.categorie || "";
      return {
        slug,
        title: titre,
        type: "methode",
        href: `/methodes/${slug}`,
        searchText: `${titre} ${description} ${mots_cles} ${categorie}`,
      };
    });
}

function collectLearn() {
  const dir = join(root, "content", "learn");
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((f) => f.endsWith(".fr.mdx") && !f.startsWith("glossaire"))
    .map((f) => {
      const raw = readFileSync(join(dir, f), "utf8");
      const { data } = matter(raw);
      const slug = data.slug || f.replace(".fr.mdx", "");
      const titre = data.titre || slug;
      const description = data.description || "";
      const mots_cles = Array.isArray(data.mots_cles) ? data.mots_cles.join(" ") : "";
      return {
        slug,
        title: titre,
        type: "apprendre",
        href: `/apprendre/${slug}`,
        searchText: `${titre} ${description} ${mots_cles}`,
      };
    });
}

function collectGlossaire() {
  const file = join(root, "content", "learn", "glossaire.fr.mdx");
  if (!existsSync(file)) return [];
  const raw = readFileSync(file, "utf8");
  const { content } = matter(raw);

  const entries = [];
  // Match ### <span id="term">Title</span> followed by paragraph
  const regex = /###\s*<span\s+id="([^"]+)">([^<]+)<\/span>\s*\n\n([^\n#]+)/g;
  let m;
  while ((m = regex.exec(content)) !== null) {
    const id = m[1];
    const title = m[2].trim();
    const desc = m[3].trim();
    entries.push({
      slug: `glossaire-${id}`,
      title,
      type: "glossaire",
      href: `/apprendre/glossaire#${id}`,
      searchText: `${title} ${desc}`,
    });
  }
  return entries;
}

// Collect all entries
const allEntries = [
  ...collectExercices(),
  ...collectMethodes(),
  ...collectLearn(),
  ...collectGlossaire(),
];

console.log(`[gen-search-index] ${allEntries.length} entries (${
  allEntries.filter((e) => e.type === "exercice").length} exercices, ${
  allEntries.filter((e) => e.type === "methode").length} méthodes, ${
  allEntries.filter((e) => e.type === "apprendre").length} apprendre, ${
  allEntries.filter((e) => e.type === "glossaire").length} glossaire)`);

// Write TypeScript file
const fileContents =
  `// This file is auto-generated by scripts/gen-search-index.mjs — DO NOT EDIT\n` +
  `export type SearchEntry = {\n` +
  `  slug: string;\n` +
  `  title: string;\n` +
  `  type: "exercice" | "methode" | "apprendre" | "glossaire";\n` +
  `  href: string;\n` +
  `  searchText: string;\n` +
  `};\n\n` +
  `export const SEARCH_INDEX: SearchEntry[] = ${JSON.stringify(allEntries, null, 2)};\n`;

mkdirSync(outputDir, { recursive: true });
writeFileSync(outputPath, fileContents, "utf8");
console.log(`[gen-search-index] Wrote ${outputPath}`);
